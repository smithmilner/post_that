<?php

class FlagsController extends BaseController {

	/**
	 * Display a listing of the resource.
	 *
	 * @return Response
	 */
	public function index()
	{
        return View::make('flags.index');
	}

	/**
	 * Show the form for creating a new resource.
	 *
	 * @return Response
	 */
	public function create()
	{
        return View::make('flags.create');
	}

	/**
	 * Store a newly created resource in storage.
	 *
	 * @return Response
	 */
	public function store()
	{
		$input = Input::all();

		$flag = new Flag($input);

		if (Sentry::getUser()->flags()->save($flag)) {
			Alert::success('Favorite Post Saved')->flash();
		}

		// Add validation errors to the page.
		// $flag->displayErrors();
		return Redirect::route('posts.show', array($input['post_id']));
	}

	/**
	 * Display the specified resource.
	 *
	 * @param  int  $id
	 * @return Response
	 */
	public function show($id)
	{
        return View::make('flags.show');
	}

	/**
	 * Show the form for editing the specified resource.
	 *
	 * @param  int  $id
	 * @return Response
	 */
	public function edit($id)
	{
        return View::make('flags.edit');
	}

	/**
	 * Update the specified resource in storage.
	 *
	 * @param  int  $id
	 * @return Response
	 */
	public function update($id)
	{
		//
	}

	/**
	 * Remove the specified resource from storage.
	 *
	 * @param  int  $id
	 * @return Response
	 */
	public function destroy($flag)
	{
		$post = Post::find($flag->post_id);

		Flag::findOrFail($flag->id)->delete();

		Alert::info('Favorite Removed')->flash();

		return Redirect::route('posts.show', array($post->id));
	}

}
